package com.bdv.infi.webservices.beans.consult;

import com.bdv.infi.webservices.beans.CredencialesDeUsuario;



public class ConsultaSaldoCuentaAEA {

	private CredencialesDeUsuario credenciales;
		
	private String numeroCuenta;
	
	private String divisa;
	
	private boolean comision;

	public boolean isComision() {
		return comision;
	}

	public void setComision(boolean comision) {
		this.comision = comision;
	}

	public CredencialesDeUsuario getCredenciales() {
		return credenciales;
	}

	public void setCredenciales(CredencialesDeUsuario credenciales) {
		this.credenciales = credenciales;
	}

	public String getDivisa() {
		return divisa;
	}

	public void setDivisa(String divisa) {
		this.divisa = divisa;
	}

	public String getNumeroCuenta() {
		return numeroCuenta;
	}

	public void setNumeroCuenta(String numeroCuenta) {
		this.numeroCuenta = numeroCuenta;
	}
}
