package com.bdv.infi.webservices.beans;

/**
 * Se utilizan en todas las consutlas para pasarle al servicio el usuario y
 * clave secreta del usuario que hizo login en la aplicaci&oacute;n o en el canal.
 * 
 * @author cet
 * 
 */
public class CredencialesDeUsuario {
	/**
	 * nombre de usuario; username
	 */
	private String nombreDeUsuario;

	/**
	 * clave secreta, clave, password
	 */
	private String claveSecreta;

	public String getClaveSecreta() {
		return claveSecreta;
	}

	public void setClaveSecreta(String claveSecreta) {
		this.claveSecreta = claveSecreta;
	}

	public String getNombreDeUsuario() {
		return nombreDeUsuario;
	}

	public void setNombreDeUsuario(String nombreDeUsuario) {
		this.nombreDeUsuario = nombreDeUsuario;
	}
}
