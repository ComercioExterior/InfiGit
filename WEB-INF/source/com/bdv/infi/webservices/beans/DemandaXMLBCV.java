package com.bdv.infi.webservices.beans;

public class DemandaXMLBCV {
	private String idOfertaBCV;
	private String refInstitucion;
	private String tasaCambio;
	private String estado;
	private String nroJornada;
	private String demandante;
	private String cliente;
	private String monto;
	
	public String getIdOfertaBCV() {
		return idOfertaBCV;
	}
	public void setIdOfertaBCV(String idOfertaBCV) {
		this.idOfertaBCV = idOfertaBCV;
	}
	public String getRifInstitucion() {
		return refInstitucion;
	}
	public void setRifInstitucion(String rifInstitucion) {
		this.refInstitucion = rifInstitucion;
	}
	public String getTasaCambio() {
		return tasaCambio;
	}
	public void setTasaCambio(String tasaCambio) {
		this.tasaCambio = tasaCambio;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getNroJornada() {
		return nroJornada;
	}
	public void setNroJornada(String nroJornada) {
		this.nroJornada = nroJornada;
	}
	public String getDemandante() {
		return demandante;
	}
	public void setDemandante(String demandante) {
		this.demandante = demandante;
	}
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	public String getMonto() {
		return monto;
	}
	public void setMonto(String monto) {
		this.monto = monto;
	}
}
