package com.bdv.infi.data;

/**
 * @author NM26659
 * Clase utilizada para el uso de los detalles de ordenes. Entidad asociada a la estructura de datos INFI_TB_220_ORDENE_DETALLE
 */
@SuppressWarnings("serial")
public class OrdenDetalle extends Orden {

	private String fechaSalidaViaje;
	private String fechaRetornoViaje;
	
	private String numeroPasaporte;
	
	private long numeroTicketClavenet;

	private long idOrden;
	
	public long getIdOrden() {
		return idOrden;
	}

	public void setIdOrden(long idOrden) {
		this.idOrden = idOrden;
	}

	public String getFechaRetornoViaje() {
		return fechaRetornoViaje;
	}

	public void setFechaRetornoViaje(String fechaRetornoViaje) {
		this.fechaRetornoViaje = fechaRetornoViaje;
	}

	public String getFechaSalidaViaje() {
		return fechaSalidaViaje;
	}

	public void setFechaSalidaViaje(String fechaSalidaViaje) {
		this.fechaSalidaViaje = fechaSalidaViaje;
	}

	public String getNumeroPasaporte() {
		return numeroPasaporte;
	}

	public void setNumeroPasaporte(String numeroPasaporte) {
		this.numeroPasaporte = numeroPasaporte;
	}

	public long getNumeroTicketClavenet() {
		return numeroTicketClavenet;
	}

	public void setNumeroTicketClavenet(long numeroTicketClavenet) {
		this.numeroTicketClavenet = numeroTicketClavenet;
	}
	
}
