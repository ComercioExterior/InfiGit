package com.bdv.infi.data;

/** 
 * Representa el detalle de la informaci&oacute;n de los registros enviados en el archivo
 */
public class Detalle {

/**
 * Muestra el contenido que se coloco en el archivo. Representa un registro
 */
private String contenido;
 
/**
 * Getter of the property <tt>contenido</tt>
 *
 * @return Returns the contenido.
 * 
 */
public String getContenido()
{
	return contenido;
}

/**
 * Setter of the property <tt>contenido</tt>
 *
 * @param contenido The contenido to set.
 *
 */
public void setContenido(String contenido ){
	this.contenido = contenido;
}

	/**
 * Id de la operaci&oacute;n
 */
private long idOperacion;
 
/**
 * Getter of the property <tt>idOperacion</tt>
 *
 * @return Returns the idOperacion.
 * 
 */
public long getIdOperacion()
{
	return idOperacion;
}

/**
 * Setter of the property <tt>idOperacion</tt>
 *
 * @param idOperacion The idOperacion to set.
 *
 */
public void setIdOperacion(long idOperacion ){
	this.idOperacion = idOperacion;
}

	/**
 * Id de la orden
 */
private long idOrden;
 
/**
 * Getter of the property <tt>idOrden</tt>
 *
 * @return Returns the idOrden.
 * 
 */
public long getIdOrden()
{
	return idOrden;
}

/**
 * Setter of the property <tt>idOrden</tt>
 *
 * @param idOrden The idOrden to set.
 *
 */
public void setIdOrden(long idOrden ){
	this.idOrden = idOrden;
}

/**Status de la linea enviada.
 * en espera, procesada, no procesada
 * */
public String status;

public String getStatus() {
	return status;
}

public void setStatus(String status) {
	this.status = status;
}

/**Descripcion del error cuando no se puede procesar la recepcion del registro*/
public String error;

/**
 * @return la variable error
 */
public String getError() {
	return error;
}

/**
 * @param error establece la variable error
 */
public void setError(String error) {
	this.error = error;
}

}