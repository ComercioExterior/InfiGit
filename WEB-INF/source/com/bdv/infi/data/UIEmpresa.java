package com.bdv.infi.data;


/**Representa las empresas asociadas a una unidad de inversi&oacute;n*/
public class UIEmpresa {
	
	/**
	 * Identificador de la Unidad de Inversion
	 */
	private long idUnidadInversion;
	/**
	 * Identificador del Empresa
	 */
	private String idEmpresa;	
	/**
	 * Identificador de los Roles de la Empresa en la Unidad de Inversion
	 */
	private String idRoles;
	/**
	 * Nombre de la persona contacto de la empresa para la unidad de inversion
	 */
	private String nombreContacto;	
	/**
	 * Telefono de la persona contacto de la empresa para la unidad de inversion
	 */
	private String telefonoContacto = " ";
	
	/**
	 * Retorna el valor del atributo Identificador de la Unidad de Inversion
	 * @return
	 */
	public long getIdUnidadInversion() {
		return idUnidadInversion;
	}
	/**
	 * Asigna valor al atributo Identificador de la Unidad de Inversion
	 * @param idUnidadInversion
	 */	
	public void setIdUnidadInversion(long idUnidadInversion) {
		this.idUnidadInversion = idUnidadInversion;
	}
	
	/**
	 * Retorna el valor del atributo Identificador ddel Empresa
	 * @return
	 */
	public String getIdEmpresa() {
		return idEmpresa;
	}
	/**
	 * Asigna valor al atributo Identificador del Empresa
	 * @param idEmpresa
	 */	
	public void setIdEmpresa(String idEmpresa) {
		this.idEmpresa = idEmpresa;
	}
	
	/**
	 * Retorna Identificador de los Roles de la Empresa
	 * @return
	 */
	public String getIdRoles() {
		return idRoles;
	}
	/**
	 * Asigna valor al Identificador de los Roles de la Empresa
	 * @param idRoles
	 */	
	public void setIdRoles(String horarioDesde) {
		this.idRoles = horarioDesde;
	}
	
	/**
	 * Retorna el valor del Nombre de la persona contacto de la empresa
	 * @return
	 */
	public String getNombreContacto() {
		return nombreContacto;
	}
	/**
	 * Asigna valor al Nombre de la persona contacto de la empresa
	 * @param nombreContacto
	 */	
	public void setNombreContacto(String nombreContacto) {
		this.nombreContacto = nombreContacto;
	}
	
	/**
	 * Retorna el valor del Telefono de la persona contacto de la empresa
	 * @return
	 */
	public String getTelefonoContacto() {
		return telefonoContacto;
	}
	/**
	 * Asigna valor al Telefono de la persona contacto de la empresa
	 * @param telefonoContacto
	 */	
	public void setTelefonoContacto(String telefonoContacto) {
		this.telefonoContacto = telefonoContacto;
	}

}
