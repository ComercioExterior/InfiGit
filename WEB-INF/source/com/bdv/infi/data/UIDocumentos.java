package com.bdv.infi.data;

public class UIDocumentos {

	/**
	 * Identificador de la Unidad de Inversion
	 */
	private long idUnidadInversion;
	/**
	 * Identificador del Tipo de Carta
	 */
	private int idTipoCarta;
	/**
	 * Identificador del Documento
	 */
	private int idDocumento;
	/**
	 * Identificador del Tipo de Persona
	 */
	private String idTipoPersona;
	/**
	 * Identificador del Blotter
	 */
	private String idBloter;
	/**
	 * Identificador Unico para cada registro creado en esta tabla
	 */
	private int idUnico;	
	
	public String getIdBloter() {
		return idBloter;
	}
	public void setIdBloter(String idBloter) {
		this.idBloter = idBloter;
	}
	public int getIdDocumento() {
		return idDocumento;
	}
	public void setIdDocumento(int idDocumento) {
		this.idDocumento = idDocumento;
	}
	public int getIdTipoCarta() {
		return idTipoCarta;
	}
	public void setIdTipoCarta(int idTipoCarta) {
		this.idTipoCarta = idTipoCarta;
	}
	public String getIdTipoPersona() {
		return idTipoPersona;
	}
	public void setIdTipoPersona(String idTipoPersona) {
		this.idTipoPersona = idTipoPersona;
	}
	public long getIdUnidadInversion() {
		return idUnidadInversion;
	}
	public void setIdUnidadInversion(long idUnidadInversion) {
		this.idUnidadInversion = idUnidadInversion;
	}
	public int getIdUnico() {
		return idUnico;
	}
	public void setIdUnico(int idUnico) {
		this.idUnico = idUnico;
	}
	
}
