package com.bdv.infi.data;

public class Pais {
	
	private String id;
	private String descripcion;
	
	public Pais(String id, String descripcion){
		this.id = id;
		this.descripcion = descripcion;
	}
	 
	public String getPaises_id()
	{
		return id;
	}


	public String getPaises_descripcion()
	{
		return descripcion;
	}
		
	
}
