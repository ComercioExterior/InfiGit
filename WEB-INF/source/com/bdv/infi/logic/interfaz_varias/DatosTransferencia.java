package com.bdv.infi.logic.interfaz_varias;

/**
 * Datos necesarios para la transferencia de archivos hacia otras interfaces
 */
public class DatosTransferencia {

	/**ruta del archivo donde debe generarse la información a enviar*/
	private String rutaArchivoFinal;
	
	/**Ip o nombre del servidor*/	
	private String ipServidor;
	
	/**Directorio final donde debe transferir el archivo final*/
	private String directorioFinalFtp;

	public String getRutaArchivoFinal() {
		return rutaArchivoFinal;
	}

	public void setRutaArchivoFinal(String archivoFinal) {
		this.rutaArchivoFinal = archivoFinal;
	}

	public String getIpServidor() {
		return ipServidor;
	}

	public void setIpServidor(String ipServidor) {
		this.ipServidor = ipServidor;
	}

	public String getDirectorioFinalFtp() {
		return directorioFinalFtp;
	}

	public void setDirectorioFinalFtp(String directorioFinalFtp) {
		this.directorioFinalFtp = directorioFinalFtp;
	}
}
