package com.bdv.infi_services.beans.entidades.mensajes_respuesta;

import java.io.Serializable;

public class OperacionFinanciera implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * Id de la Orden
	 */
	public String idOrden;
	/**
	 * Id de la operacion financiera
	 */
	public String idOperacionFinanciera;
	/**
	 * Status en que quedo la orden
	 */
	public String status;
	/**
	 * Error en caso de generarlo
	 */
	public String error;
	/**
	 * @return the error
	 */
	public String getError() {
		return error;
	}
	/**
	 * @param error the error to set
	 */
	public void setError(String error) {
		this.error = error;
	}
	/**
	 * @return the idOperacionFinanciera
	 */
	public String getIdOperacionFinanciera() {
		return idOperacionFinanciera;
	}
	/**
	 * @param idOperacionFinanciera the idOperacionFinanciera to set
	 */
	public void setIdOperacionFinanciera(String idOperacionFinanciera) {
		this.idOperacionFinanciera = idOperacionFinanciera;
	}
	/**
	 * @return the idOrden
	 */
	public String getIdOrden() {
		return idOrden;
	}
	/**
	 * @param idOrden the idOrden to set
	 */
	public void setIdOrden(String idOrden) {
		this.idOrden = idOrden;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
}
