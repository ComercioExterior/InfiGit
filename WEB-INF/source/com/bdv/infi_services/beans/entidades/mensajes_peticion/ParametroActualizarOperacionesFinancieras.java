package com.bdv.infi_services.beans.entidades.mensajes_peticion;

import java.io.Serializable;

public class ParametroActualizarOperacionesFinancieras implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Credenciales credenciales;
	/**
	 * Id de la orden
	 */
	public String idOrden;
	/**
	 * Id de las Operaciones Financieras
	 */
	public String idOperacionFinanciera;
	/**
	 * Numero de cheque de la operacion financiera
	 */
	public String numeroCheque;
	/**
	 * @return the idOperacionFinanciera
	 */
	public String getIdOperacionFinanciera() {
		return idOperacionFinanciera;
	}
	/**
	 * @param idOperacionFinanciera the idOperacionFinanciera to set
	 */
	public void setIdOperacionFinanciera(String idOperacionFinanciera) {
		this.idOperacionFinanciera = idOperacionFinanciera;
	}
	/**
	 * @return the idOrden
	 */
	public String getIdOrden() {
		return idOrden;
	}
	/**
	 * @param idOrden the idOrden to set
	 */
	public void setIdOrden(String idOrden) {
		this.idOrden = idOrden;
	}
	/**
	 * @return the numeroCheque
	 */
	public String getNumeroCheque() {
		return numeroCheque;
	}
	/**
	 * @param numeroCheque the numeroCheque to set
	 */
	public void setNumeroCheque(String numeroCheque) {
		this.numeroCheque = numeroCheque;
	}
	/**
	 * @return the credenciales
	 */
	public Credenciales getCredenciales() {
		return credenciales;
	}
	/**
	 * @param credenciales the credenciales to set
	 */
	public void setCredenciales(Credenciales credenciales) {
		this.credenciales = credenciales;
	}
	
}
