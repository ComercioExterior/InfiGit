package org.bcv.serviceINTERVENCION;

public class moneda_autorizada {

	private String token;
	private String coMonedaIso;
	private String nbMonedaIso;
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getCoMonedaIso() {
		return coMonedaIso;
	}
	public void setCoMonedaIso(String coMonedaIso) {
		this.coMonedaIso = coMonedaIso;
	}
	public String getNbMonedaIso() {
		return nbMonedaIso;
	}
	public void setNbMonedaIso(String nbMonedaIso) {
		this.nbMonedaIso = nbMonedaIso;
	}
}
